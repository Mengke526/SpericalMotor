function dfLambda = dfLambdaFit(sigma)
dfLambda = 0;
t = ceil(sigma/60);
if (t==1) % sigma角度在0-60度范围内
    %a = [-0.002148  -0.001992	0.003018    0.002388	-0.00029    -0.000829    -0.000242];
    %b = [0               -0.01152	 -0.01244	  -0.006054	  -0.001912	 -0.0006988	 -0.0002408];
    phi = [-1.57079632679490,-2.97036912684115,2.90358647969076,2.76587993946786,-2.99106629731569,-2.27117932338806,-2.35370899406782];
    A = [0.00214800000000000,0.0116909565049229,0.0128008563776022,0.00650795359540924,0.00193386762732096,0.00108423357262169,0.000341392208464107];
    w = 0.07273;
    for i = 1:length(phi)
        %dfLambda = dfLambda+a(i)*cos((i-1)*w*sigma)+b(i)*sin((i-1)*w*sigma);
        dfLambda = dfLambda+A(i)*sin((i-1)*w*sigma + phi(i));
    end
end
end